export function DashboardNav({ locale }: { locale: string }) {
  const pathname = usePathname()

  const links = [
    { href: `/${locale}/portfolio`, label: 'Portfolio', Icon: BarChart2Icon },
    {
      href: `/${locale}/transactions`,
      label: 'Transactions',
      Icon: ArrowLeftRightIcon,
    },
    { href: `/${locale}/accounts`, label: 'Accounts', Icon: Building2Icon },
    { href: `/${locale}/categories`, label: 'Categories', Icon: TagIcon },
    { href: `/${locale}/crypts`, label: 'Crypts', Icon: CoinsIcon },
  ]

  return (
    <nav className="flex-1 space-y-1 p-4">
      {links.map((link) => {
        const isActive = pathname.startsWith(link.href)
        return (
          <Link
            key={link.href}
            href={link.href}
            className={`flex items-center gap-3 rounded-md px-3 py-2.5 text-sm font-medium transition-colors ${
              isActive
                ? 'border-l-2 border-emerald-500 bg-[#1e293b] pl-[10px] text-white'
                : 'text-slate-400 hover:bg-[#1e293b] hover:text-white'
            }`}
          >
            <link.Icon />
            {link.label}
          </Link>
        )
      })}
    </nav>
  )
}
